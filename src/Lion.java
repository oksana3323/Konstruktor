public class Lion {

    private  String paroda;
    private double kg ;

    public Lion(String paroda, double kg) {
        this.paroda = paroda;
        this.kg = kg;
    }

    public String getParoda() {
        return paroda;
    }

    public void setParoda(String paroda) {
        this.paroda = paroda;
    }

    public double getKg() {
        return kg;
    }

    public void setKg(double kg) {
        this.kg = kg;
    }
}
